-- Переключение между кодом и neo-tree по Ctrl-l
keymap.set('n', '<C-l>', function()
  local current_win = vim.api.nvim_get_current_win()

  -- Ищем окно с neo-tree
  local neo_tree_win = nil
  for _, win in ipairs(vim.api.nvim_list_wins()) do
    local buf = vim.api.nvim_win_get_buf(win)
    local ft = vim.api.nvim_buf_get_option(buf, "filetype")
    if ft == "neo-tree" then
      neo_tree_win = win
      break
    end
  end

  if neo_tree_win then
    -- Если neo-tree открыт
    if current_win == neo_tree_win then
      -- Если фокус на neo-tree, переключаемся на код
      vim.cmd("wincmd p") -- Переход к предыдущему окну
    else
      -- Если фокус на коде, переключаемся на neo-tree
      vim.api.nvim_set_current_win(neo_tree_win)
    end
  else
    -- Если neo-tree не открыт, открываем его и переводим фокус
    vim.cmd("Neotree focus")
  end
end, { noremap = true, silent = true })
